/* MUSIC */
let playing=false;
const audio=document.getElementById('bgMusic');
const mIcon=document.getElementById('musicIcon');
const mWave=document.getElementById('musicWave');
function tryAutoplay(){
  audio.volume=0.35;
  audio.play().then(()=>{ playing=true; mIcon.className='fas fa-pause'; mWave.classList.remove('paused'); }).catch(()=>{ mIcon.className='fas fa-play'; mWave.classList.add('paused'); });
}
function toggleMusic(){
  if(playing){ audio.pause(); playing=false; mIcon.className='fas fa-play'; mWave.classList.add('paused'); }
  else{ audio.play(); playing=true; mIcon.className='fas fa-pause'; mWave.classList.remove('paused'); }
}
document.addEventListener('click',function f(){ if(!playing) tryAutoplay(); document.removeEventListener('click',f); },{once:true});
tryAutoplay();

