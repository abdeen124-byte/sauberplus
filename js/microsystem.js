let lang = localStorage.getItem('ms_lang') || 'ar';
function setLang(l) {
  lang=l; localStorage.setItem('ms_lang',l);
  const html=document.documentElement; html.lang=l; html.dir=l==='ar'?'rtl':'ltr';
  document.querySelectorAll('[data-key]').forEach(el=>{ const v=T[l][el.dataset.key]; if(v!==undefined) el.innerHTML=v; });
  document.querySelectorAll('.lang-btn').forEach(b=>{ b.classList.toggle('active',b.dataset.lang===l); });
  const arr=document.getElementById('heroArrow');
  if(arr) arr.className=l==='ar'?'fas fa-arrow-left':'fas fa-arrow-right';
}
